    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        String seleccionado = (String) comboLanzamiento.getSelectedItem();
        if (seleccionado.equals("Norte")) {
            padre.getPanel().getJuego().setLineaSalida(Juego.NORTE);
        } else if (seleccionado.equals("Sur")) {
            padre.getPanel().getJuego().setLineaSalida(Juego.SUR);
        } else if (seleccionado.equals("Oeste")) {
            padre.getPanel().getJuego().setLineaSalida(Juego.OESTE);
        } else if (seleccionado.equals("Este")) {
            padre.getPanel().getJuego().setLineaSalida(Juego.ESTE);
        }
        padre.getBtnAgregarAeropuerto().setEnabled(true);
        padre.getBtnFinEdicion().setEnabled(true);
        padre.getBtnDefinirLineaLanzamiento().setEnabled(false);
        padre.getPanel().repaint();

        dispose();
    }//GEN-LAST:event_btnOkActionPerformed
