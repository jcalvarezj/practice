    private void btnCargarMapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarMapaActionPerformed
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de texto (*.txt)", "txt");
        JFileChooser jfc = new JFileChooser();
        jfc.setFileFilter(filtro);
        jfc.setDialogTitle("Abrir archivo de texto de configuracion de mapa");
        int seleccion = jfc.showOpenDialog(this);
        if(seleccion == JFileChooser.APPROVE_OPTION)
        {
            try {
                File f = jfc.getSelectedFile();
                Juego config = manejador.cargarConfiguracion(f.getPath(), panel.getJuego());
                System.out.println("#lkfjhnklfhndjklf "+config.getAeropuertos());
                panel.setJuego(config);
                panel.repaint();
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "No se ha encontrado el archivo en la ruta especificada", "Archivo no encontrado!", JOptionPane.ERROR_MESSAGE);
            } catch (ArchivoNoValidoExcepion ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Archivo no valido!", JOptionPane.ERROR_MESSAGE);
            } catch (AeropuertoNombreExistenteException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Problema!", JOptionPane.ERROR_MESSAGE);
            }
        }
        btnFinEdicion.setEnabled(true);
    }//GEN-LAST:event_btnCargarMapaActionPerformed

